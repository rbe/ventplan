    private void entfernenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entfernenButtonActionPerformed
        DefaultListModel model = null;
        Object[] values = ausgewTeilstreckenList.getSelectedValues();
        
        model = (DefaultListModel) ausgewTeilstreckenList.getModel();
        for (int i = 0; i < values.length; i++) {
            model.removeElement(values[i]);
        }
        
        model = (DefaultListModel) verfgbTeilstreckenList.getModel();
        for (int i = 0; i < values.length; i++) {
            model.addElement(values[i]);
        }
        
        updateUebernommeneTeilstrecken();
    }//GEN-LAST:event_entfernenButtonActionPerformed
    
    private void hinzufuegenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hinzufuegenButtonActionPerformed
        DefaultListModel model = null;
        Object[] values = verfgbTeilstreckenList.getSelectedValues();
        
        model = (DefaultListModel) ausgewTeilstreckenList.getModel();
        for (int i = 0; i < values.length; i++) {
            model.addElement(values[i]);
        }
        
        model = (DefaultListModel) verfgbTeilstreckenList.getModel();
        for (int i = 0; i < values.length; i++) {
            model.removeElement(values[i]);
        }
        
        updateUebernommeneTeilstrecken();
    }//GEN-LAST:event_hinzufuegenButtonActionPerformed
    
