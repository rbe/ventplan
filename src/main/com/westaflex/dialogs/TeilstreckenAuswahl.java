    private void updateUebernommeneTeilstrecken() {
        
        ListModel model = null;
        int size = -1;
        StringBuffer sb = new StringBuffer();
        
        uebernTeilstreckenTextField.setText("");
        
        model = ausgewTeilstreckenList.getModel();
        size = model.getSize();
        for (int i = 0; i < size; i++) {
            sb.append(model.getElementAt(i)).append("; ");
        }
        
        uebernTeilstreckenTextField.setText(sb.toString());
        
    }
    
