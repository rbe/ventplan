    public void setTeilstrecken(String[] verfgbTeilstrecken, String[] ausgewTeilstrecken) {
        DefaultListModel model = new DefaultListModel();
        
        for (int i = 0; i < verfgbTeilstrecken.length; i++) {
            model.addElement(verfgbTeilstrecken[i]);
        }
        verfgbTeilstreckenList.setModel(model);
        ausgewTeilstreckenList.setModel(new DefaultListModel());
    }
    
